    private void listapedidos() {
        try {
            PedidosDAO objpedidosdao = new PedidosDAO();
            DefaultTableModel model = (DefaultTableModel) tabela_pedido.getModel();
            model.setNumRows(0);

            ArrayList<PedidosDTO> lista = objpedidosdao.listarpedidos();
            for (int i = 0; i < lista.size(); i++) {
                model.addRow(new Object[]{
                    lista.get(i).getId(),
                    lista.get(i).getNome(),
                    lista.get(i).getEmail(),
                    lista.get(i).getDatacriada(),
                    lista.get(i).getDatacoleta(),
                    lista.get(i).getCriadopor(),
                    lista.get(i).getTipo(),
                    lista.get(i).getRua(),
                    lista.get(i).getCidade(),
                    lista.get(i).getNumcasa()
                });
            }

        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "listapedidos: " + erro);
        }
    }

