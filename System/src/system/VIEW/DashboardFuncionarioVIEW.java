    public void ids() {

        try {
            PedidosDAO objpedidosdao = new PedidosDAO();
            ResultSet rs = objpedidosdao.totalid();
            int ids, n_ids = 0;
            while (rs.next()) {
                ids = rs.getInt("id");
                n_ids += 1;
            }
            total_ids.setText(Integer.toString(n_ids));

        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "Inicio função: " + erro);
        }

    }

    public void tipos() {

        try {
            PedidosDAO objpedidosdao = new PedidosDAO();
            ResultSet rs = objpedidosdao.tipos();
            int p = 0, m = 0, g = 0;
            String tipo;
            while (rs.next()) {
                tipo = rs.getString("type_pedido");
                System.out.println(tipo);
                if ("P".equals(tipo)) {
                    p += 1;
                } else if ("M".equals(tipo)) {
                    m += 1;
                } else {
                    g += 1;
                }
            }
            total_p.setText(Integer.toString(p));
            total_m.setText(Integer.toString(m));
            total_g.setText(Integer.toString(g));

        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "Inicio função: " + erro);
        }

    }

